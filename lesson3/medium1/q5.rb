# this variable is not visible inside the method definition fora fib()
limit = 15

# instead, we can pass limit in as an argument
def fib(first_num, second_num, limit)
  while first_num + second_num < limit
    sum = first_num + second_num
    first_num = second_num
    second_num = sum
  end
  sum
end

result = fib(0, 1, 15)
puts "result is #{result}"